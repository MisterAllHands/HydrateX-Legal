// Language utilities
const LanguageUtils = {
    supportedLanguages: ['en', 'de', 'es', 'fr', 'ja', 'ru'],

    getCurrentLanguage() {
        const stored = localStorage.getItem('hydratex-lang');
        if (stored && this.supportedLanguages.includes(stored)) {
            return stored;
        }

        // Auto-detect from browser
        const browserLang = navigator.language.split('-')[0];
        if (this.supportedLanguages.includes(browserLang)) {
            return browserLang;
        }

        return 'en';
    },

    setLanguage(lang) {
        if (this.supportedLanguages.includes(lang)) {
            localStorage.setItem('hydratex-lang', lang);
            this.applyLanguage(lang);
        }
    },

    applyLanguage(lang) {
        document.documentElement.lang = lang;

        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const keys = key.split('.');
            let value = translations;

            for (const k of keys) {
                value = value?.[k];
            }

            if (value?.[lang]) {
                el.innerHTML = value[lang];
            }
        });

        // Update language selector display
        const selector = document.getElementById('language-selector');
        if (selector) {
            selector.value = lang;
        }

        // Trigger custom event for page-specific updates
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    },

    init() {
        const lang = this.getCurrentLanguage();
        this.applyLanguage(lang);

        // Setup language selector if exists
        const selector = document.getElementById('language-selector');
        if (selector) {
            selector.value = lang;
            selector.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
            });
        }
    }
};

